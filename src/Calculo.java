
public class Calculo {
	
	public static int soma(int a, int b) {
		return a + b;	
	}
	
	public static double areac(double a) {
		
		   double pi = 3.14159;
		   return pi*a*a;
	}
	public static int diferenca(int a, int b, int c, int d) {
		   return (a*b)-(c*d);
		   }
	
	public static double salario(double horas, double valorHora) {
		return horas*valorHora;
	}
	
	
}
